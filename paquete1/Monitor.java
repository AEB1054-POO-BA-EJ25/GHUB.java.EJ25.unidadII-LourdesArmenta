package paquete1;

public class Monitor {
    //propiedades
    private String marca;
    private int tamanio;

    //constructores
    public Monitor(String marca, int tamanio) {
        this.marca = marca;
        this.tamanio = tamanio;
    }

    //getters
    public String getMarca() {
        return marca;
    }

    public int getTamanio() {
        return tamanio;
    }

    

    
    
}
